/**
 * 방어 포켓몬의 타입(1개 또는 2개)에 대한 최종 상성 배율을 계산합니다.
 *
 * @param attackType - 공격 기술의 타입
 * @param defenderTypes - 방어 포켓몬의 타입 배열 (1~2개)
 * @returns 최종 배율 (0 | 0.25 | 0.5 | 1 | 2 | 4)
 *
 * @example
 * getTypeEffectiveness('water', ['fire', 'rock']) // 4 (물 → 불꽃/바위)
 * getTypeEffectiveness('ground', ['flying'])       // 0 (땅 → 비행: 무효)
 */
export function getTypeEffectiveness(
  attackType: PokemonType,
  defenderTypes: PokemonType[],
): number {
  return defenderTypes.reduce(
    (acc, defType) => acc * (TYPE_CHART[attackType][defType] ?? 1),
    1,
  );
}

/**
 * 데미지에 적용할 상성 메시지를 반환합니다.
 * 배틀 로그 UI에서 사용합니다.
 *
 * @param effectiveness - getTypeEffectiveness의 반환값
 * @returns 한국어 상성 메시지 (없으면 null)
 */
export function getEffectivenessMessage(effectiveness: number): string | null {
  if (effectiveness === 0) return '효과가 없는 것 같다...';
  if (effectiveness < 1) return '효과가 별로인 것 같다...';
  if (effectiveness > 1) return '효과가 굉장했다!';
  return null; // 1배는 메시지 없음
}
